public class meatType {
    private String meatType;
    private double meatPrice;

    public meatType(String meatType) {
        this.meatType = meatType;
        this.meatPrice = 1.49;
    }

    public String getMeatType() {
        return meatType;
    }

    public double getMeatPrice() {
        return meatPrice;
    }
}
